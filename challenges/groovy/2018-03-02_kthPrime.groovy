What is the kth prime number (1-based)?

Example
For k = 1, the output should be
kthPrime(k) = 2.

Input/Output

[execution time limit] 6 seconds (groovy)

[input] integer k

Index of prime searched.

Guaranteed constraints:
0 < k < 100000.

[output] integer

The kth prime number.


